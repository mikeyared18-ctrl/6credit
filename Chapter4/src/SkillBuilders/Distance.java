package SkillBuilders;

public class Distance {
    public static void main(String[] args) {
        // Declare variables for the race segments
        double segment1 = 12.2;
        double segment2 = 10.6;
        double segment3 = 5.8;

        // Calculate total distance
        double totalDistance = segment1 + segment2 + segment3;

        // Display the result
        System.out.println("Segment 1: " + segment1 + " km");
        System.out.println("Segment 2: " + segment2 + " km");
        System.out.println("Segment 3: " + segment3 + " km");
        System.out.println("Total race distance: " + totalDistance + " km");
    }
}
